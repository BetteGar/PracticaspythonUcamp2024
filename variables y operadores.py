# 1. Las variables son:
    # 1. Dinámicamente tipadas (significa que pueden cambiar de formato).
    # 2. Fuertemente tipadas (significa que no se puede cambiar de formato).

# 2. Tipos básicos de variables:
    # int: Enteros, incluye también binarios o hexadecimales.
    # float: Decimales.
    # bool: Boleanos (True-False, 0-1).
    # complex: Complejos (parte imaginaria explícita '1+3j').
    # str: Caracteres, pueden ser números letras o símbolos, 'entre comillas'.

# 3. Nomenclatura:
    # Comenzar con letras o guión bajo, no números.
    # Sin espacios y sin símbolos, solo guión bajo.
    # Son sensitivas a minúsculas y mayúsculas.

# 4. Consejos:
    # Nombrarlas en minúsculas.
    # No palabras reservadas.
    # No usar acentos.

# Y sobre los operadores, debes recordar lo siguiente:
    # Aritméticos:
        # +, -
        # *, /, %, //
        # En strings sólo aplica + y *
    # Comparación:
        # ==, ¡=, <, <=, >, >=
